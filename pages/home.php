<h3>Open Source</h3>
<p> "Open source is a development method for software that harnesses the power of distributed peer review and transparency of process. The promise of open source is better quality, higher reliability, more flexibility, lower cost, and an end to predatory vendor lock-in." </p>
<p> Open Source can never be explained within a bunch of lines in a paragraph, but in simple terms - "It is a software Philosophy visualized, first by Richard Stallman and quickly adopted by smart minds all over the world. A philosophy based on Freedom, from Proprietory Vendor Software and grants the right to access the 'Human Readable form of the software(Source Code), and to redistribute and share the software', free of cost " </p>
<p> To people who nodd, Open Source is nothing they have to concern - probably your every google search may cost you, without Open Source Technologies. Every time you access Facebook, you did that free of cost, thanks to Open Source Technologies. As Computer Science Engineers we have a compelling reason to <a href="http://dl.dropbox.com/u/2814392/asharma-osi-what-is-opensource-presentation.pdf">Learn More</a> about Open Source Development Process, Tools and Technologies.  </p>
